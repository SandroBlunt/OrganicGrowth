describe("classify — phase-scoped blocking (AC2)", () => {
  // --- block × research ---

  it("bad Apify token → block on research (code: apify_token_invalid)", () => {
    const findings = classify(withOverrides({ apifyTokenValid: false }));
    const match = findingsWhere(findings, { severity: "block", phase: "research", code: "apify_token_invalid" });
    assert.equal(match.length, 1, "expected exactly one apify_token_invalid finding");
  });

  it("bad Apify token → no block on production", () => {
    const findings = classify(withOverrides({ apifyTokenValid: false }));
    const productionBlocks = findingsWhere(findings, { severity: "block", phase: "production" });
    assert.equal(productionBlocks.length, 0, "bad Apify token must not block production");
  });

  it("bad Apify token → no block on publish", () => {
    const findings = classify(withOverrides({ apifyTokenValid: false }));
    const publishBlocks = findingsWhere(findings, { severity: "block", phase: "publish" });
    assert.equal(publishBlocks.length, 0, "bad Apify token must not block publish");
  });

  it("no valid seed (seedCount: 0) → block on research (code: no_valid_seed)", () => {
    const findings = classify(withOverrides({ seedCount: 0 }));
    const match = findingsWhere(findings, { severity: "block", phase: "research", code: "no_valid_seed" });
    assert.equal(match.length, 1, "expected exactly one no_valid_seed finding");
  });

  it("no valid seed → no block on production or publish", () => {
    const findings = classify(withOverrides({ seedCount: 0 }));
    const nonResearchBlocks = findings.filter(
      (f) => f.severity === "block" && f.phase !== "research",
    );
    assert.equal(nonResearchBlocks.length, 0, "no_valid_seed must only block research");
  });

  // --- advisory × research + block × production (Space inaccessible) ---

  it("inaccessible Space → advisory on research (code: space_inaccessible_advisory)", () => {
    const findings = classify(withOverrides({ spaceAccessible: false }));
    const match = findingsWhere(findings, {
      severity: "advisory",
      phase: "research",
      code: "space_inaccessible_advisory",
    });
    assert.equal(match.length, 1, "expected one space_inaccessible_advisory finding");
  });

  it("inaccessible Space → block on production (code: space_inaccessible)", () => {
    const findings = classify(withOverrides({ spaceAccessible: false }));
    const match = findingsWhere(findings, {
      severity: "block",
      phase: "production",
      code: "space_inaccessible",
    });
    assert.equal(match.length, 1, "expected one space_inaccessible block on production");
  });

  it("inaccessible Space → no block on research", () => {
    const findings = classify(withOverrides({ spaceAccessible: false }));
    const researchBlocks = findingsWhere(findings, { severity: "block", phase: "research" });
    assert.equal(researchBlocks.length, 0, "Space inaccessible must not block research");
  });

  it("inaccessible Space → no block on publish", () => {
    const findings = classify(withOverrides({ spaceAccessible: false }));
    const publishBlocks = findingsWhere(findings, { severity: "block", phase: "publish" });
    assert.equal(publishBlocks.length, 0, "Space inaccessible must not block publish");
  });

  // --- advisory × research + block × production (credits low) ---

  it("insufficient credits → advisory on research (code: credits_low_advisory)", () => {
    const findings = classify(withOverrides({ creditsOk: false }));
    const match = findingsWhere(findings, {
      severity: "advisory",
      phase: "research",
      code: "credits_low_advisory",
    });
    assert.equal(match.length, 1, "expected one credits_low_advisory finding");
  });

  it("insufficient credits → block on production (code: credits_low)", () => {
    const findings = classify(withOverrides({ creditsOk: false }));
    const match = findingsWhere(findings, {
      severity: "block",
      phase: "production",
      code: "credits_low",
    });
    assert.equal(match.length, 1, "expected one credits_low block on production");
  });

  it("insufficient credits → no block on research", () => {
    const findings = classify(withOverrides({ creditsOk: false }));
    const researchBlocks = findingsWhere(findings, { severity: "block", phase: "research" });
    assert.equal(researchBlocks.length, 0, "insufficient credits must not block research");
  });

  it("insufficient credits → no block on publish", () => {
    const findings = classify(withOverrides({ creditsOk: false }));
    const publishBlocks = findingsWhere(findings, { severity: "block", phase: "publish" });
    assert.equal(publishBlocks.length, 0, "insufficient credits must not block publish");
  });

  // --- block × publish ---

  it("missing Channel URL → block on publish (code: channel_url_missing)", () => {
    const findings = classify(withOverrides({ channelUrl: null }));
    const match = findingsWhere(findings, {
      severity: "block",
      phase: "publish",
      code: "channel_url_missing",
    });
    assert.equal(match.length, 1, "expected one channel_url_missing block on publish");
  });

  it("missing Channel URL → no block on research", () => {
    const findings = classify(withOverrides({ channelUrl: null }));
    const researchBlocks = findingsWhere(findings, { severity: "block", phase: "research" });
    assert.equal(researchBlocks.length, 0, "missing channel URL must not block research");
  });

  it("missing Channel URL → no block on production", () => {
    const findings = classify(withOverrides({ channelUrl: null }));
    const productionBlocks = findingsWhere(findings, { severity: "block", phase: "production" });
    assert.equal(productionBlocks.length, 0, "missing channel URL must not block production");
  });
});

