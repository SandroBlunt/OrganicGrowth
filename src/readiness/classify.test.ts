describe("classify — deterministic ordering (AC5)", () => {
  it("same inputs always produce the same finding list", () => {
    const inputs: ReadinessInputs = {
      apifyTokenValid: false,
      seedCount: 0,
      offNicheSeedCount: 0,
      spaceAccessible: false,
      creditsOk: false,
      channelUrl: null,
      baseline: null,
      bannedWordsEmpty: false,
    };
    const first = classify(inputs);
    const second = classify(inputs);
    assert.deepEqual(first, second, "classify must be deterministic");
  });

  it("findings are ordered: research < production < publish (by phase)", () => {
    const inputs: ReadinessInputs = {
      apifyTokenValid: false,
      seedCount: 0,
      offNicheSeedCount: 0,
      spaceAccessible: false,
      creditsOk: false,
      channelUrl: null,
      baseline: null,
      bannedWordsEmpty: false,
    };
    const findings = classify(inputs);
    const phaseOrder = new Map([["research", 0], ["production", 1], ["publish", 2]]);
    for (let i = 1; i < findings.length; i++) {
      const prev = phaseOrder.get(findings[i - 1]!.phase) ?? 0;
      const curr = phaseOrder.get(findings[i]!.phase) ?? 0;
      assert.ok(prev <= curr, `finding[${i - 1}].phase=${findings[i - 1]!.phase} must come before finding[${i}].phase=${findings[i]!.phase}`);
    }
  });

  it("within the same phase, block findings come before advisory findings", () => {
    const inputs2: ReadinessInputs = {
      apifyTokenValid: false, // block research
      seedCount: 0,           // block research
      offNicheSeedCount: 0,
      spaceAccessible: false, // advisory research + block production
      creditsOk: false,       // advisory research + block production
      channelUrl: null,       // block publish
      baseline: null,         // advisory research
      bannedWordsEmpty: false,
    };
    const findings = classify(inputs2);
    // Within each phase group, verify blocks precede advisories
    const phases: Array<"research" | "production" | "publish"> = ["research", "production", "publish"];
    for (const phase of phases) {
      const group = findings.filter((f) => f.phase === phase);
      let seenAdvisory = false;
      for (const f of group) {
        if (f.severity === "advisory") seenAdvisory = true;
        if (f.severity === "block") {
          assert.ok(!seenAdvisory, `block finding with code=${f.code} appeared after an advisory in phase=${phase}`);
        }
      }
    }
  });

  it("within same phase+severity, findings are ordered alphabetically by code", () => {
    // Trigger two research blocks: apify_token_invalid and no_valid_seed
    const inputs = withOverrides({ apifyTokenValid: false, seedCount: 0 });
    const findings = classify(inputs);
    const researchBlocks = findings.filter((f) => f.phase === "research" && f.severity === "block");
    for (let i = 1; i < researchBlocks.length; i++) {
      const prev = researchBlocks[i - 1]!.code;
      const curr = researchBlocks[i]!.code;
      assert.ok(
        prev <= curr,
        `research blocks must be alphabetically ordered by code: ${prev} should come before ${curr}`,
      );
    }
  });
});

