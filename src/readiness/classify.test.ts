/**
 * Tests for the readiness classifier — `classify(inputs): Finding[]`.
 *
 * AC6: ALL tests are pure — they pass literal in-memory `ReadinessInputs` objects.
 * No disk, no Magnific Space, no Apify, no network. No fake is needed because `classify`
 * has no I/O boundary — the conductor performs the probes and passes results in as inputs.
 *
 * Test matrix covers every severity×phase combination:
 *   block × research    — apify_token_invalid, no_valid_seed
 *   advisory × research — space_inaccessible_advisory, credits_low_advisory, null_baseline,
 *                         off_niche_seed, empty_banned_words
 *   block × production  — space_inaccessible, credits_low
 *   block × publish     — channel_url_missing
 *   (advisory × production and advisory × publish are not generated by classify — deliberate)
 */

import { describe, it } from "node:test";
import assert from "node:assert/strict";
import { classify } from "./classify.ts";
import type { Finding, ReadinessInputs } from "./types.ts";

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** A fully-healthy inputs object — no findings expected. */
const HEALTHY: ReadinessInputs = {
  apifyTokenValid: true,
  seedCount: 3,
  offNicheSeedCount: 0,
  spaceAccessible: true,
  creditsOk: true,
  channelUrl: "https://www.facebook.com/testpage",
  baseline: 0.5,
  bannedWordsEmpty: false,
};

function withOverrides(overrides: Partial<ReadinessInputs>): ReadinessInputs {
  return { ...HEALTHY, ...overrides };
}

function findingsWhere(findings: Finding[], pred: Partial<Finding>): Finding[] {
  return findings.filter((f) =>
    Object.entries(pred).every(([k, v]) => (f as unknown as Record<string, unknown>)[k] === v),
  );
}

// ---------------------------------------------------------------------------
// AC1: classify returns Finding[] with severity and phase fields
// ---------------------------------------------------------------------------

describe("classify — return shape (AC1)", () => {
  it("returns an array", () => {
    const result = classify(HEALTHY);
    assert.ok(Array.isArray(result), "classify must return an array");
  });

  it("each finding has severity, phase, code, and message fields", () => {
    const result = classify(withOverrides({ apifyTokenValid: false }));
    assert.ok(result.length > 0, "expected at least one finding");
    for (const f of result) {
      assert.ok(
        f.severity === "block" || f.severity === "advisory",
        `unexpected severity: ${f.severity}`,
      );
      assert.ok(
        f.phase === "research" || f.phase === "production" || f.phase === "publish",
        `unexpected phase: ${f.phase}`,
      );
      assert.equal(typeof f.code, "string");
      assert.ok(f.code.length > 0, "code must be non-empty");
      assert.equal(typeof f.message, "string");
      assert.ok(f.message.length > 0, "message must be non-empty");
    }
  });

  it("severity is exactly 'block' or 'advisory' — no other value", () => {
    const allInputs: ReadinessInputs[] = [
      HEALTHY,
      withOverrides({ apifyTokenValid: false }),
      withOverrides({ seedCount: 0 }),
      withOverrides({ spaceAccessible: false }),
      withOverrides({ creditsOk: false }),
      withOverrides({ channelUrl: null }),
      withOverrides({ baseline: null }),
    ];
    for (const inp of allInputs) {
      for (const f of classify(inp)) {
        assert.ok(
          f.severity === "block" || f.severity === "advisory",
          `unexpected severity: ${f.severity}`,
        );
      }
    }
  });

  it("phase is exactly 'research', 'production', or 'publish' — no other value", () => {
    const allInputs: ReadinessInputs[] = [
      HEALTHY,
      withOverrides({ apifyTokenValid: false }),
      withOverrides({ seedCount: 0 }),
      withOverrides({ spaceAccessible: false }),
      withOverrides({ creditsOk: false }),
      withOverrides({ channelUrl: null }),
      withOverrides({ baseline: null }),
    ];
    for (const inp of allInputs) {
      for (const f of classify(inp)) {
        assert.ok(
          f.phase === "research" || f.phase === "production" || f.phase === "publish",
          `unexpected phase: ${f.phase}`,
        );
      }
    }
  });
});

