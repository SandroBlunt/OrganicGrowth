describe("classify — multiple problems produce multiple findings", () => {
  it("all-bad inputs → at least 6 findings (one per condition plus advisory+block pairs)", () => {
    const allBad: ReadinessInputs = {
      apifyTokenValid: false,
      seedCount: 0,
      offNicheSeedCount: 0,
      spaceAccessible: false,
      creditsOk: false,
      channelUrl: null,
      baseline: null,
      bannedWordsEmpty: false,
    };
    const findings = classify(allBad);
    // apify_token_invalid(research block) + no_valid_seed(research block) +
    // space_inaccessible_advisory(research) + space_inaccessible(production block) +
    // credits_low_advisory(research) + credits_low(production block) +
    // channel_url_missing(publish block) + null_baseline(research advisory) = 8
    assert.ok(findings.length >= 6, `expected at least 6 findings, got ${findings.length}`);
  });

  it("all-bad inputs → covers research block, production block, publish block, and advisories", () => {
    const allBad: ReadinessInputs = {
      apifyTokenValid: false,
      seedCount: 0,
      offNicheSeedCount: 0,
      spaceAccessible: false,
      creditsOk: false,
      channelUrl: null,
      baseline: null,
      bannedWordsEmpty: false,
    };
    const findings = classify(allBad);
    assert.ok(
      findings.some((f) => f.severity === "block" && f.phase === "research"),
      "must have a block on research",
    );
    assert.ok(
      findings.some((f) => f.severity === "block" && f.phase === "production"),
      "must have a block on production",
    );
    assert.ok(
      findings.some((f) => f.severity === "block" && f.phase === "publish"),
      "must have a block on publish",
    );
    assert.ok(
      findings.some((f) => f.severity === "advisory"),
      "must have at least one advisory",
    );
  });
});

